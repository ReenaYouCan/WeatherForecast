package com.leftshift.weatherreport.pojo;

public class CityName {
	String _cityname;
	int _citynumber;

	public String getCityName() {
		return _cityname;
	}

	public void setCityName(String name) {
		this._cityname = name;
	}

	public int getCityNumber() {
		return _citynumber;
	}

	public void setCityNumber(int num) {
		this._citynumber = num;
	}
}
